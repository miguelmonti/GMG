    public void combobox() {
        try {
            conexion = DriverManager.getConnection("jdbc:mysql://localhost/sistema_gmg", "root", "1405");

            st = conexion.createStatement();

            rs = st.executeQuery("Select ciclo from ciclos;");
            while (rs.next()) {
                this.jcb_ciclo.addItem(rs.getString("ciclo"));
            }

            rs = st.executeQuery("Select id_alumno,nombre_alum,ap_paterno_alum, ap_materno_alum from alumnos;");
            while (rs.next()) {
                this.jcb_alumno.addItem(rs.getString(2) + " " + rs.getString(3) + " " + rs.getString(4));
                this.jcb_id_alumno.addItem(rs.getString(1));
            }

        } catch (SQLException err) {

            //JOptionPane.showMessageDialog(null, "Error " + err.getMessage());
        }
    }

