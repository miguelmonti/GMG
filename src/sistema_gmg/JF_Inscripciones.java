    private void jcb_cicloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcb_cicloActionPerformed
        try {
            conexion = DriverManager.getConnection("jdbc:mysql://localhost/sistema_gmg", "root", "1405");
            st = conexion.createStatement();

            ResultSet rs = st.executeQuery("select id_ciclo from ciclos where ciclo = '" + this.jcb_ciclo.getSelectedItem() + "'");
            rs.next();
            this.jtf_id_ciclo.setText(String.valueOf(rs.getString("id_ciclo")));

            this.jcb_grado.removeAllItems();
            this.jcb_id_grado.removeAllItems();
            rs = st.executeQuery("Select id_grupo,grado,grupo from grupos where id_ciclo = '" + this.jtf_id_ciclo.getText() + "';");
            while (rs.next()) {
                this.jcb_grado.addItem(rs.getString(2) + " - " + rs.getString(3));
                this.jcb_id_grado.addItem(rs.getString(1));
            }

        } catch (Exception e) {
            // JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jcb_cicloActionPerformed

