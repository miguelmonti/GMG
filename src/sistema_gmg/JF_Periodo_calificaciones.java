    private void actualizar_registro() {
        try {

            jtf_nombre_periodo.getText();
            jtf_fecha_inicio.getText();
            jtf_fecha_termino.getText();

            st.executeUpdate("update periodo_calif set nombre_periodo= '" + jtf_nombre_periodo.getText() + "', fecha_inicio = '" + jtf_fecha_inicio.getText() + "', fecha_termino = '" + jtf_fecha_termino.getText() + "' where id_periodo= "
                    + this.jtf_id_periodo.getText());

            Conectar();
            this.primer_registro();

        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Error " + err.getMessage());
        }
    }

    private void guardar_registro() {
        try {
            String nombre_periodo = this.jtf_nombre_periodo.getText();
            String fecha_inicio = this.jtf_fecha_inicio.getText();
            String fecha_termino = this.jtf_fecha_termino.getText();

            st.executeUpdate("Insert into periodo_calif (nombre_periodo,fecha_inicio,fecha_termino)" + "values ('" + nombre_periodo + "','" + fecha_termino + "','" + fecha_inicio + "');");
            
            JOptionPane.showMessageDialog(null, "Registro Guardado Exitosamente", "Guardado", JOptionPane.INFORMATION_MESSAGE);
            Conectar();
            this.primer_registro();

        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Error " + err.getMessage());
        }

    }

    private void siguiente_registro() {
        try {
            if (rs.isLast() == false) {
                rs.next();
                this.jtf_id_periodo.setText(rs.getString("id_periodo"));
                this.jtf_nombre_periodo.setText(rs.getString("nombre_periodo"));
                this.jtf_fecha_inicio.setText(rs.getString("fecha_inicio"));
                this.jtf_fecha_termino.setText(rs.getString("fecha_termino"));
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Error " + err.getMessage());
        }
    }

