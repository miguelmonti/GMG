    private void buscar() {
        try {            
            conexion = DriverManager.getConnection("jdbc:mysql://localhost/sistema_gmg", "root", "1405");

            st = conexion.createStatement();
            
            rs = st.executeQuery("select * from ciclos where ciclo like '%" + this.jtf_buscar.getText() + "%'");

            jtf_id_ciclo.setText(null);
            jtf_ciclo.setText(null);
            jtf_finicio.setText(null);
            jtf_ftermino.setText(null);
            
            jt_ciclos.setModel(DbUtils.resultSetToTableModel(rs));
            
            jb_cancelar.setEnabled(true);            
            rs = st.executeQuery("select *from ciclos");
        } catch (SQLException ex) {
            Logger.getLogger(JF_Alumnos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
